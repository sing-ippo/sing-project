START_ID = 2000

WORD_PATTERN = re.compile(r'^[a-zа-яё]+(?:-[a-zа-яё]+)*$')

russian_stopwords.update(extra_stopwords)

mystem = MorphAnalyzer()

def log(message):
    print(f"[{datetime.now().strftime('%H:%M:%S')}] {message}")
def lemmatize_text(text):
    """
    Приводит слова к начальной форме, фильтруя мусор и стоп-слова.
    Использует регулярное выражение для проверки валидности слов.
    """
    
    tokens = nltk.word_tokenize(text.lower(), language="russian")
    lemmas = []
    
    for word in tokens:
        if len(word) < 3:
            continue
            
        if WORD_PATTERN.match(word):
            parse_result = mystem.parse(word)[0]
            lemma = parse_result.normal_form
            if lemma not in russian_stopwords and parse_result.tag.POS != 'NPRO':
                lemmas.append(lemma)
            
    return lemmas

def normalize_question(lemmas):
    """
    Создает нормализованную строку из списка лемм для сравнения.
    Принимает на вход уже готовый список лемм.
    """
    if not lemmas:
        return ""
    return " ".join(sorted(list(set(lemmas))))

# Категории


def categorize_question(lemmas):
    """
    Определяет категорию вопроса. Если совпадений нет, 
    возвращает 'учебный_процесс' согласно регламенту.
    """
    words_set = set(lemmas)
    
    for cat, cat_keywords in categories.items():
        if any(kw in words_set for kw in cat_keywords):
            return cat
            
    return "учебный_процесс"

# ===== ОСНОВНАЯ ФУНКЦИЯ КОНВЕРТАЦИИ =====

def convert_chats(input_file, output_file="faq_chats.json", min_frequency=3):
    """
    Конвертирует student_questions.json в faq.json

    Параметры:
    - input_file: путь к входному JSON-файлу
    - output_file: путь к выходному JSON-файлу
    - min_frequency: минимальная частота вопроса для включения
    """
    log(f"🔄 Загружаем {input_file}...")
    with open(input_file, 'r', encoding='utf-8') as f:
        data = json.load(f)

    log(f"📖 Загружено записей: {len(data)}")

    # Фильтрация по частоте
    filtered = [item for item in data if item.get('frequency', 1) >= min_frequency]
    log(f"📊 После фильтрации (frequency >= {min_frequency}): {len(filtered)}")

    # Конвертация
    faq_entries = []
    skipped = 0
    new_id = START_ID
    for item in filtered:
        question = item.get('question', '')
        if not question:
            skipped += 1
            continue
        lemmas = lemmatize_text(question)
        cat = categorize_question(lemmas)
        if cat in categories:
            keywords = list(set(lemmas) & set(categories[cat]))
        else:
            keywords = list(set(lemmas))

        if not keywords:
            if lemmas:
                keywords = list(set(lemmas))
            else:
                keywords = [cat]

        faq_entries.append({
            "id": new_id,
            "question": question,
            "answer": None,  # Из чатов нет ответов ,
            "keywords": keywords,
            "category": cat,
            "source": item.get('source', 'chat_unknown')
        })
        new_id+=1

    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(faq_entries, f, ensure_ascii=False, indent=4)
    
    print(f"✅ Конвертировано и сохранено в {output_file}: {len(faq_entries)}")
    if skipped:
        log(f"⚠️ Пропущено (пустой вопрос): {skipped}")

    return faq_entries


