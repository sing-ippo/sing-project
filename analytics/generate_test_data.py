NO_ANSWER = "Извините, я не нашёл ответа на ваш вопрос в базе знаний."


def generate_timestamp(base_date: datetime, hour_offset: int) -> str:
    dt = base_date + timedelta(hours=hour_offset)
    dt = dt.replace(minute=random.randint(0, 59), second=random.randint(0, 59))
    return dt.strftime("%Y-%m-%dT%H:%M:%S")


def working_hour_offset(max_days: int = 7) -> int:
    """Случайный offset в часах в рамках рабочих часов (8:00–22:00)."""
    hour_offset = random.randint(0, max_days * 24)
    hour_of_day = hour_offset % 24
    if hour_of_day < 8:
        hour_offset += 8 - hour_of_day
    elif hour_of_day > 22:
        hour_offset -= hour_of_day - 22
    return hour_offset


def generate_requests(count: int = 200) -> list[dict[str, Any]]:
    """Генерирует логи запросов бота."""
    base_date = datetime(2026, 3, 1, 8, 0, 0)
    records: list[dict[str, Any]] = []

    for _ in range(count):
        category = random.choices(
            list(CATEGORIES.keys()), weights=list(CATEGORIES.values()), k=1
        )[0]

        question, answer = random.choice(PAIRS_BY_CATEGORY[category])
        matched = random.random() < 0.80

        if matched:
            helpful_rand = random.random()
            if helpful_rand < 0.70:
                helpful: bool | None = True
            elif helpful_rand < 0.90:
                helpful = False
            else:
                helpful = None
        else:
            helpful = None
            answer = NO_ANSWER

        timestamp = generate_timestamp(base_date, working_hour_offset())

        records.append(
            {
                "timestamp": timestamp,
                "query": question.lower().rstrip("?"),
                "answer": answer,
                "matched": matched,
                "helpful": helpful,
            }
        )

    records.sort(key=lambda x: x["timestamp"])
    return records


def generate_suggestions(count: int = 30) -> list[dict[str, Any]]:
    """Генерирует предложения пользователей."""
    base_date = datetime(2026, 3, 1, 8, 0, 0)
    types = ["new_question", "new_answer", "question_with_answer"]
    type_weights = [0.7, 0.2, 0.1]
    records: list[dict[str, Any]] = []

    for _ in range(count):
        category = random.choices(
            list(CATEGORIES.keys()), weights=list(CATEGORIES.values()), k=1
        )[0]

        question, answer = random.choice(PAIRS_BY_CATEGORY[category])
        user = random.choice(USER_NAMES)
        suggestion_type = random.choices(types, weights=type_weights, k=1)[0]

        suggestion_answer: str | None = (
            None if suggestion_type == "new_question" else f"Пользователь предлагает: {answer}"
        )

        timestamp = generate_timestamp(base_date, working_hour_offset())

        records.append(
            {
                "timestamp": timestamp,
                "user": user,
                "type": suggestion_type,
                "question": question,
                "answer": suggestion_answer,
            }
        )

    records.sort(key=lambda x: x["timestamp"])
    return records


def write_jsonl(data: list[dict[str, Any]], file_path: Path) -> None:
    """Записывает данные в формате JSONL."""
    file_path.parent.mkdir(parents=True, exist_ok=True)
    with file_path.open("w", encoding="utf-8") as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")


def main() -> None:
    parser = argparse.ArgumentParser(description="Генератор тестовых данных для дашборда")
    parser.add_argument("--seed", type=int, default=None, help="Фиксированный seed для воспроизводимости")
    args = parser.parse_args()

    if args.seed is not None:
        random.seed(args.seed)
        log.info("Seed: %d", args.seed)

    log.info("Генерация тестовых данных для дашборда аналитики...")

    requests = generate_requests(200)
    suggestions = generate_suggestions(30)

    base = Path(__file__).parent / "test_data"
    requests_path = base / "requests.jsonl"
    suggestions_path = base / "suggestions.jsonl"

    write_jsonl(requests, requests_path)
    write_jsonl(suggestions, suggestions_path)

    log.info("Создано %d записей в %s", len(requests), requests_path)
    log.info("Создано %d записей в %s", len(suggestions), suggestions_path)
    log.info("Готово! Данные можно загрузить в dashboard.html")


if __name__ == "__main__":
    main()
