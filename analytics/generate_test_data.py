# Пользователи для suggestions
USER_NAMES = [
    "Алексей", "Дмитрий", "Максим", "Андрей", "Иван",
    "Мария", "Анна", "Екатерина", "Ольга", "София",
    "user123", "student_2025", "mirea_fan", "first_year",
]


def generate_timestamp(base_date: datetime, offset_hours: int) -> str:
    """Генерирует timestamp"""
    dt = base_date + timedelta(hours=offset_hours)
    dt = dt.replace(minute=random.randint(0, 59), second=random.randint(0, 59))
    return dt.strftime("%Y-%m-%dT%H:%M:%S")


def generate_requests(count: int = 200) -> list[dict]:
    """Генерирует логи запросов бота."""
    requests = []
    base_date = datetime(2026, 3, 1, 8, 0, 0)

    # Распределяем запросы по времени (7 дней)
    for i in range(count):
        # Выбираем категорию по весам
        category = random.choices(
            list(CATEGORIES.keys()),
            weights=list(CATEGORIES.values()),
            k=1
        )[0]

        # Выбираем вопрос и ответ
        question = random.choice(QUESTIONS_BY_CATEGORY[category])
        answer = random.choice(ANSWERS[category])

        # matched: 80% запросов находят ответ
        matched = random.random() < 0.80

        # helpful: если matched, то 70% положительных отзывов
        if matched:
            helpful_rand = random.random()
            if helpful_rand < 0.70:
                helpful = True
            elif helpful_rand < 0.90:
                helpful = False
            else:
                helpful = None  # Не оставил отзыв
        else:
            helpful = None
            answer = "Извините, я не нашёл ответа на ваш вопрос в базе знаний."

        # Разброс по времени
        hour_offset = random.randint(0, 7 * 24)
        # Смещаем к рабочим часам
        hour_of_day = (hour_offset % 24)
        if hour_of_day < 8:
            hour_offset += (8 - hour_of_day)
        elif hour_of_day > 22:
            hour_offset -= (hour_of_day - 22)

        timestamp = generate_timestamp(base_date, hour_offset)

        requests.append({
            "timestamp": timestamp,
            "query": question.lower().rstrip("?"),
            "answer": answer,
            "matched": matched,
            "helpful": helpful,
        })

    # Сортируем по timestamp
    requests.sort(key=lambda x: x["timestamp"])
    return requests


def generate_suggestions(count: int = 30) -> list[dict]:
    """Генерирует предложения пользователей."""
    suggestions = []
    base_date = datetime(2026, 3, 1, 8, 0, 0)

    # Типы предложений
    types = ["new_question", "new_answer", "question_with_answer"]
    type_weights = [0.7, 0.2, 0.1]

    for i in range(count):
        category = random.choices(
            list(CATEGORIES.keys()),
            weights=list(CATEGORIES.values()),
            k=1
        )[0]

        question = random.choice(QUESTIONS_BY_CATEGORY[category])
        user = random.choice(USER_NAMES)
        suggestion_type = random.choices(types, weights=type_weights, k=1)[0]

        # answer только для типов new_answer и question_with_answer
        if suggestion_type == "new_question":
            answer = None
        else:
            answer = f"Пользователь предлагает: {random.choice(ANSWERS[category])}"

        hour_offset = random.randint(0, 7 * 24)
        timestamp = generate_timestamp(base_date, hour_offset)

        suggestions.append({
            "timestamp": timestamp,
            "user": user,
            "type": suggestion_type,
            "question": question,
            "answer": answer,
        })

    # Сортируем по timestamp
    suggestions.sort(key=lambda x: x["timestamp"])
    return suggestions


def write_jsonl(data: list[dict], file_path: str) -> None:
    """Записывает данные в формате JSONL."""
    os.makedirs(os.path.dirname(file_path), exist_ok=True)
    with open(file_path, "w", encoding="utf-8") as f:
        for item in data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")


def main():
    """Основная функция."""
    print("Генерация тестовых данных для дашборда аналитики...")

    # Генерируем данные
    requests = generate_requests(200)
    suggestions = generate_suggestions(30)

    # Записываем файлы
    requests_path = "test_data/requests.jsonl"
    suggestions_path = "test_data/suggestions.jsonl"

    write_jsonl(requests, requests_path)
    write_jsonl(suggestions, suggestions_path)

    print(f"Создано {len(requests)} записей в {requests_path}")
    print(f"Создано {len(suggestions)} записей в {suggestions_path}")
    print("\nГотово! Данные можно загрузить в dashboard.html")


if __name__ == "__main__":
    main()
